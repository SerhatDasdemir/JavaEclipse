package day13_methods_Part1;

public class PassMultipleArguments {

	public static void main(String[] args) {
		
		showSum(5,10);
		showSum(50,15);
		showSum(56,1);
		showSum(500,1000);
		
		
	}

	public static void showSum(int num1, int num2) {
		
		System.out.println(num1+num2);
		
	}
	
}
